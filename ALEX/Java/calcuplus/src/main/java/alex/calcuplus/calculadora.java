    private void jButton13ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton13ActionPerformed
        this.num2=Float.parseFloat(this.pantalla.getText());/*La tecla = debe recoger el segundo numero que estara escrito en la pantalla*/
        switch(this.operador)/*Segun el valor que vaya dando cada tecla a la variable operador, asi funcionaran los cases*/
        {
            case"+": this.pantalla.setText(sincero(this.num1+this.num2));/*cuando operador valga + sacaremos por pantalla this.num1+this.num2,
            pero convirtiendolos en String con (Float.toString())*/
            break;
            case"-": this.pantalla.setText(sincero(this.num1-this.num2));
            break;
            case"*": this.pantalla.setText(sincero(this.num1*this.num2));
            break;
            case"/": if (this.num2==0)
            {   this.pantalla.setText("No se puede dividir entre 0");
            }
            else{
                this.pantalla.setText(sincero(this.num1/this.num2));
            break;
            }
        }
                
    }//GEN-LAST:event_jButton13ActionPerformed

