    public String sincero(float resultado)/*Creamos esta funcion para que cuando devuelva un entero no ponga .0*/
    {
     String retorno="";
     retorno=Float.toString(resultado);
     if (resultado%1==0)
     {
         retorno=retorno.substring(0,retorno.length()-2);
     }
        return retorno;
    }
